/**
 * Runs the program. Creates an instance of OneTimePad and runs it.
 *
 * @author Wil Simpson
 */
public class OneTimePadDriver
{
    /**
     * Initializes and starts the program. No command line arguments are needed.
     *
     * @param args command line arguments
     */
    public static void main(String[] args)
    {
        OneTimePad program = new OneTimePad();
        program.run();
    }
}
